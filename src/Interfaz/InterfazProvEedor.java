    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        if (txtNit.getText().isEmpty() || txtNombre.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "El NIT y el Nombre son obligatorios");
        } else {
            prov.setNit(Integer.parseInt(txtNit.getText()));
            prov.setNombre(txtNombre.getText());
            prov.setTelefono(txtTelefono.getText());
            prov.setDireccion(txtDireccion.getText());
            prov.setCiudad(txtCiudad.getText());
            
            if (provDAO.registrarProveedor(prov)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Proveedor registrado con éxito");
                limpiarCampos();
                listarProveedoresEnTabla();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar el proveedor");
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (txtNit.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona o digita el NIT del proveedor a modificar");
        } else {
            prov.setNit(Integer.parseInt(txtNit.getText()));
            prov.setNombre(txtNombre.getText());
            prov.setTelefono(txtTelefono.getText());
            prov.setDireccion(txtDireccion.getText());
            prov.setCiudad(txtCiudad.getText());
            
            if (provDAO.modificarProveedor(prov)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Proveedor modificado correctamente");
                limpiarCampos();
                listarProveedoresEnTabla();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al modificar");
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (txtNit.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Digita el NIT del proveedor que deseas eliminar");
        } else {
            int pregunta = javax.swing.JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar este proveedor?");
            if (pregunta == javax.swing.JOptionPane.YES_OPTION) {
                int nit = Integer.parseInt(txtNit.getText());
                if (provDAO.eliminarProveedor(nit)) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Proveedor eliminado");
                    limpiarCampos();
                    listarProveedoresEnTabla();
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar");
                }
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

