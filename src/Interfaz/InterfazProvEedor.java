    public void listarProveedoresEnTabla() {
        java.util.List<Modelo.Proveedor> lista = provDAO.listarProveedores();
        modeloTabla = (javax.swing.table.DefaultTableModel) tablaProveedores.getModel();
        
        // Limpiamos la tabla por si tenía datos viejos
        modeloTabla.setRowCount(0);
        
        Object[] ob = new Object[5];
        for (int i = 0; i < lista.size(); i++) {
            ob[0] = lista.get(i).getNit();
            ob[1] = lista.get(i).getNombre();
            ob[2] = lista.get(i).getTelefono();
            ob[3] = lista.get(i).getDireccion();
            ob[4] = lista.get(i).getCiudad();
            modeloTabla.addRow(ob);
        }
        tablaProveedores.setModel(modeloTabla);
    }
  
    

